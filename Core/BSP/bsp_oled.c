/**
 * @brief       OLED显示一个字符
 * @param       line: 行 (1-4)
 * @param       column: 列 (1-16)
 * @param       ch: 要显示的字符
 */
void OLED_ShowChar(uint8_t line, uint8_t column, char ch)
{
    uint8_t i;
    uint8_t c = ch - ' ';   /* 获取字符在字库中的偏移 */
    
    if (c > 94) c = 0;      /* 超出范围显示空格 */
    
    /* 显示上半部分 */
    OLED_SetCursor((line - 1) * 2, (column - 1) * 8);
    for (i = 0; i < 8; i++)
    {
        OLED_WriteData(OLED_F8x16[c][i]);
    }
    
    /* 显示下半部分 */
    OLED_SetCursor((line - 1) * 2 + 1, (column - 1) * 8);
    for (i = 0; i < 8; i++)
    {
        OLED_WriteData(OLED_F8x16[c][i + 8]);
    }
}

/**
 * @brief       OLED显示字符串
 * @param       line: 行 (1-4)
 * @param       column: 列 (1-16)
 * @param       str: 要显示的字符串
 */
void OLED_ShowString(uint8_t line, uint8_t column, const char *str)
{
    while (*str != '\0')
    {
        OLED_ShowChar(line, column, *str);
        column++;
        if (column > 16)    /* 自动换行 */
        {
            column = 1;
            line++;
            if (line > 4)
            {
                break;
            }
        }
        str++;
    }
}

/**
 * @brief       OLED次方函数 (内部使用)
 */
static uint32_t oled_pow(uint32_t x, uint32_t y)
{
    uint32_t result = 1;
    while (y--)
    {
        result *= x;
    }
    return result;
}

/**
 * @brief       OLED显示无符号数字
 * @param       line: 行 (1-4)
 * @param       column: 列 (1-16)
 * @param       num: 数字
 * @param       len: 显示长度
 */
void OLED_ShowNum(uint8_t line, uint8_t column, uint32_t num, uint8_t len)
{
    uint8_t i;
    
    for (i = 0; i < len; i++)
    {
        OLED_ShowChar(line, column + i, num / oled_pow(10, len - i - 1) % 10 + '0');
    }
}

/**
 * @brief       OLED显示有符号数字
 * @param       line: 行 (1-4)
 * @param       column: 列 (1-16)
 * @param       num: 数字
 * @param       len: 显示长度
 */
void OLED_ShowSignedNum(uint8_t line, uint8_t column, int32_t num, uint8_t len)
{
    uint32_t number;
    
    if (num >= 0)
    {
        OLED_ShowChar(line, column, '+');
        number = num;
    }
    else
    {
        OLED_ShowChar(line, column, '-');
        number = -num;
    }
    
    OLED_ShowNum(line, column + 1, number, len);
}

/**
 * @brief       OLED显示十六进制数
 * @param       line: 行 (1-4)
 * @param       column: 列 (1-16)
 * @param       num: 数字
 * @param       len: 显示长度
 */
void OLED_ShowHexNum(uint8_t line, uint8_t column, uint32_t num, uint8_t len)
{
    uint8_t i, digit;
    
    for (i = 0; i < len; i++)
    {
        digit = num / oled_pow(16, len - i - 1) % 16;
        if (digit < 10)
        {
            OLED_ShowChar(line, column + i, digit + '0');
        }
        else
        {
            OLED_ShowChar(line, column + i, digit - 10 + 'A');
        }
    }
}
