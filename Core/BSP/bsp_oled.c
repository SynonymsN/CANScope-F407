/**
 * @brief       I2C延时函数
 */
static void oled_delay(void)
{
    uint32_t i;
    for (i = 0; i < 10; i++)
    {
        __NOP();
    }
}

/**
 * @brief       I2C GPIO初始化
 */
static void oled_i2c_init(void)
{
    GPIO_InitTypeDef gpio_init_struct;

    OLED_SCL_GPIO_CLK_ENABLE();
    OLED_SDA_GPIO_CLK_ENABLE();

    /* SCL - 推挽输出 */
    gpio_init_struct.Pin = OLED_SCL_GPIO_PIN;
    gpio_init_struct.Mode = GPIO_MODE_OUTPUT_PP;
    gpio_init_struct.Pull = GPIO_PULLUP;
    gpio_init_struct.Speed = GPIO_SPEED_FREQ_VERY_HIGH;
    HAL_GPIO_Init(OLED_SCL_GPIO_PORT, &gpio_init_struct);

    /* SDA - 开漏输出 */
    gpio_init_struct.Pin = OLED_SDA_GPIO_PIN;
    gpio_init_struct.Mode = GPIO_MODE_OUTPUT_OD;
    HAL_GPIO_Init(OLED_SDA_GPIO_PORT, &gpio_init_struct);

    OLED_SCL(1);
    OLED_SDA(1);
}

/**
 * @brief       I2C起始信号
 */
static void oled_i2c_start(void)
{
    OLED_SDA(1);
    OLED_SCL(1);
    oled_delay();
    OLED_SDA(0);
    oled_delay();
    OLED_SCL(0);
    oled_delay();
}

/**
 * @brief       I2C停止信号
 */
static void oled_i2c_stop(void)
{
    OLED_SDA(0);
    oled_delay();
    OLED_SCL(1);
    oled_delay();
    OLED_SDA(1);
    oled_delay();
}

/**
 * @brief       I2C发送一个字节
 * @param       data: 要发送的数据
 */
static void oled_i2c_send_byte(uint8_t data)
{
    uint8_t i;
    
    for (i = 0; i < 8; i++)
    {
        OLED_SDA((data & 0x80) >> 7);
        oled_delay();
        OLED_SCL(1);
        oled_delay();
        OLED_SCL(0);
        data <<= 1;
    }
    
    /* 等待ACK (忽略应答) */
    OLED_SDA(1);
    oled_delay();
    OLED_SCL(1);
    oled_delay();
    OLED_SCL(0);
    oled_delay();
}

