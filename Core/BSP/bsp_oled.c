/**
 * @brief       OLED写命令
 * @param       cmd: 命令字节
 */
void OLED_WriteCommand(uint8_t cmd)
{
    oled_i2c_start();
    oled_i2c_send_byte(OLED_I2C_ADDR);  /* 从机地址 */
    oled_i2c_send_byte(0x00);            /* 写命令 */
    oled_i2c_send_byte(cmd);
    oled_i2c_stop();
}

/**
 * @brief       OLED写数据
 * @param       data: 数据字节
 */
void OLED_WriteData(uint8_t data)
{
    oled_i2c_start();
    oled_i2c_send_byte(OLED_I2C_ADDR);  /* 从机地址 */
    oled_i2c_send_byte(0x40);            /* 写数据 */
    oled_i2c_send_byte(data);
    oled_i2c_stop();
}

/**
 * @brief       OLED设置光标位置
 * @param       y: 页地址 (0-7)
 * @param       x: 列地址 (0-127)
 */
void OLED_SetCursor(uint8_t y, uint8_t x)
{
    OLED_WriteCommand(0xB0 | y);                    /* 设置页地址 */
    OLED_WriteCommand(0x00 | (x & 0x0F));           /* 设置列地址低4位 */
    OLED_WriteCommand(0x10 | ((x >> 4) & 0x0F));    /* 设置列地址高4位 */
}

/**
 * @brief       OLED清屏
 */
void OLED_Clear(void)
{
    uint8_t i, j;
    
    for (i = 0; i < 8; i++)
    {
        OLED_SetCursor(i, 0);
        for (j = 0; j < 128; j++)
        {
            OLED_WriteData(0x00);
        }
    }
}

/**
 * @brief       OLED初始化
 */
void OLED_Init(void)
{
    oled_i2c_init();
    
    HAL_Delay(100);     /* 等待OLED上电稳定 */
    
    /* SSD1306初始化序列 */
    OLED_WriteCommand(0xAE);    /* 关闭显示 */
    
    OLED_WriteCommand(0xD5);    /* 设置时钟分频因子,震荡频率 */
    OLED_WriteCommand(0x80);    /* [3:0]分频因子,[7:4]震荡频率 */
    
    OLED_WriteCommand(0xA8);    /* 设置驱动路数 */
    OLED_WriteCommand(0x3F);    /* 默认0x3F(1/64) */
    
    OLED_WriteCommand(0xD3);    /* 设置显示偏移 */
    OLED_WriteCommand(0x00);    /* 默认为0 */
    
    OLED_WriteCommand(0x40);    /* 设置显示开始行 [5:0] */
    
    OLED_WriteCommand(0xA1);    /* 段重定向设置,bit0:0,0->0;1,0->127; */
    OLED_WriteCommand(0xC8);    /* 设置COM扫描方向;bit3:0,普通模式;1,重定义模式 COM[N-1]->COM0;N:驱动路数 */
    
    OLED_WriteCommand(0xDA);    /* 设置COM硬件引脚配置 */
    OLED_WriteCommand(0x12);    /* [5:4]配置 */
    
    OLED_WriteCommand(0x81);    /* 对比度设置 */
    OLED_WriteCommand(0xCF);    /* 1~255;默认0x7F (亮度设置,越大越亮) */
    
    OLED_WriteCommand(0xD9);    /* 设置预充电周期 */
    OLED_WriteCommand(0xF1);    /* [3:0]PHASE 1;[7:4]PHASE 2; */
    
    OLED_WriteCommand(0xDB);    /* 设置VCOMH 电压倍率 */
    OLED_WriteCommand(0x30);    /* [6:4] 000,0.65*vcc;001,0.77*vcc;011,0.83*vcc; */
    
    OLED_WriteCommand(0xA4);    /* 全局显示开启;bit0:1,开启;0,关闭;(白屏/黑屏) */
    OLED_WriteCommand(0xA6);    /* 设置显示方式;bit0:1,反相显示;0,正常显示 */
    
    OLED_WriteCommand(0x8D);    /* 设置电荷泵 */
    OLED_WriteCommand(0x14);    /* bit2,开启/关闭 */
    
    OLED_WriteCommand(0xAF);    /* 开启显示 */
    
    OLED_Clear();               /* 清屏 */
}

