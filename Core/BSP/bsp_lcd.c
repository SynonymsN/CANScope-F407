/* 调试输出 */
#include <stdio.h>
#include "usart.h"
static void LCD_Debug(const char *fmt, ...)
{
    char buf[128];
    va_list args;
    va_start(args, fmt);
    vsnprintf(buf, sizeof(buf), fmt, args);
    va_end(args);
    HAL_UART_Transmit(&huart1, (uint8_t*)buf, strlen(buf), 100);
}

/* SRAM MSP初始化 - 被HAL_SRAM_Init调用 */
void HAL_SRAM_MspInit(SRAM_HandleTypeDef *hsram)
{
    (void)hsram;
    GPIO_InitTypeDef gpio_init_struct;
    
    __HAL_RCC_FSMC_CLK_ENABLE();
    __HAL_RCC_GPIOD_CLK_ENABLE();
    __HAL_RCC_GPIOE_CLK_ENABLE();
    __HAL_RCC_GPIOF_CLK_ENABLE();
    __HAL_RCC_GPIOG_CLK_ENABLE();
    
    /* PD0,1,8,9,10,14,15 - 数据线D2,D3,D13,D14,D15,D0,D1 */
    gpio_init_struct.Pin = GPIO_PIN_0 | GPIO_PIN_1 | GPIO_PIN_8 | GPIO_PIN_9 | 
                           GPIO_PIN_10 | GPIO_PIN_14 | GPIO_PIN_15;
    gpio_init_struct.Mode = GPIO_MODE_AF_PP;
    gpio_init_struct.Pull = GPIO_PULLUP;
    gpio_init_struct.Speed = GPIO_SPEED_FREQ_HIGH;
    gpio_init_struct.Alternate = GPIO_AF12_FSMC;
    HAL_GPIO_Init(GPIOD, &gpio_init_struct);
    
    /* PD4 - RD (NOE), PD5 - WR (NWE) */
    gpio_init_struct.Pin = GPIO_PIN_4 | GPIO_PIN_5;
    HAL_GPIO_Init(GPIOD, &gpio_init_struct);
    
    /* PE7-15 - 数据线D4-D12 */
    gpio_init_struct.Pin = GPIO_PIN_7 | GPIO_PIN_8 | GPIO_PIN_9 | GPIO_PIN_10 |
                           GPIO_PIN_11 | GPIO_PIN_12 | GPIO_PIN_13 | GPIO_PIN_14 | GPIO_PIN_15;
    HAL_GPIO_Init(GPIOE, &gpio_init_struct);
    
    /* PF12: A6 (RS信号) */
    gpio_init_struct.Pin = GPIO_PIN_12;
    HAL_GPIO_Init(GPIOF, &gpio_init_struct);
    
    /* PG12: NE4 (片选) */
    gpio_init_struct.Pin = GPIO_PIN_12;
    HAL_GPIO_Init(GPIOG, &gpio_init_struct);
}

