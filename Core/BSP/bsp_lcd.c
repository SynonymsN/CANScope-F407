/* 设置扫描方向 - 设置0x36寄存器 */
static void lcd_scan_dir(void)
{
    uint16_t regval = 0;
    
    /* 对于ILI9341/ST7789,需要设置BGR位(bit3) */
    if(lcddev.id == 0x9341 || lcddev.id == 0x7789) {
        regval = 0x08;  /* BGR=1 */
    }
    
    /* 设置0x36寄存器 */
    LCD->LCD_REG = 0x36;
    LCD->LCD_RAM = regval;
    
    /* 设置显示区域大小 */
    LCD->LCD_REG = lcddev.setxcmd;
    LCD->LCD_RAM = 0;
    LCD->LCD_RAM = 0;
    LCD->LCD_RAM = (lcddev.width - 1) >> 8;
    LCD->LCD_RAM = (lcddev.width - 1) & 0xFF;
    
    LCD->LCD_REG = lcddev.setycmd;
    LCD->LCD_RAM = 0;
    LCD->LCD_RAM = 0;
    LCD->LCD_RAM = (lcddev.height - 1) >> 8;
    LCD->LCD_RAM = (lcddev.height - 1) & 0xFF;
}

/* 设置窗口 */
static void lcd_set_window(uint16_t sx, uint16_t sy, uint16_t width, uint16_t height)
{
    uint16_t twidth = sx + width - 1;
    uint16_t theight = sy + height - 1;
    
    LCD->LCD_REG = lcddev.setxcmd;
    LCD->LCD_RAM = sx >> 8;
    LCD->LCD_RAM = sx & 0xFF;
    LCD->LCD_RAM = twidth >> 8;
    LCD->LCD_RAM = twidth & 0xFF;
    
    LCD->LCD_REG = lcddev.setycmd;
    LCD->LCD_RAM = sy >> 8;
    LCD->LCD_RAM = sy & 0xFF;
    LCD->LCD_RAM = theight >> 8;
    LCD->LCD_RAM = theight & 0xFF;
}

/**
  * @brief  LCD初始化
  */
void BSP_LCD_Init(void)
{
    GPIO_InitTypeDef gpio_init_struct;
    FSMC_NORSRAM_TimingTypeDef fsmc_read_handle;
    FSMC_NORSRAM_TimingTypeDef fsmc_write_handle;
    
    /* 使能GPIO时钟 */
    __HAL_RCC_GPIOB_CLK_ENABLE();
    
    /* PB15 - 背光 */
    gpio_init_struct.Pin = GPIO_PIN_15;
    gpio_init_struct.Mode = GPIO_MODE_OUTPUT_PP;
    gpio_init_struct.Pull = GPIO_PULLUP;
    gpio_init_struct.Speed = GPIO_SPEED_FREQ_HIGH;
    HAL_GPIO_Init(GPIOB, &gpio_init_struct);
    
    /* FSMC配置 */
    g_sram_handle.Instance = FSMC_NORSRAM_DEVICE;
    g_sram_handle.Extended = FSMC_NORSRAM_EXTENDED_DEVICE;
    g_sram_handle.Init.NSBank = FSMC_NORSRAM_BANK4;
    g_sram_handle.Init.DataAddressMux = FSMC_DATA_ADDRESS_MUX_DISABLE;
    g_sram_handle.Init.MemoryType = FSMC_MEMORY_TYPE_SRAM;
    g_sram_handle.Init.MemoryDataWidth = FSMC_NORSRAM_MEM_BUS_WIDTH_16;
    g_sram_handle.Init.BurstAccessMode = FSMC_BURST_ACCESS_MODE_DISABLE;
    g_sram_handle.Init.WaitSignalPolarity = FSMC_WAIT_SIGNAL_POLARITY_LOW;
    g_sram_handle.Init.WaitSignalActive = FSMC_WAIT_TIMING_BEFORE_WS;
    g_sram_handle.Init.WriteOperation = FSMC_WRITE_OPERATION_ENABLE;
    g_sram_handle.Init.WaitSignal = FSMC_WAIT_SIGNAL_DISABLE;
    g_sram_handle.Init.ExtendedMode = FSMC_EXTENDED_MODE_ENABLE;
    g_sram_handle.Init.AsynchronousWait = FSMC_ASYNCHRONOUS_WAIT_DISABLE;
    g_sram_handle.Init.WriteBurst = FSMC_WRITE_BURST_DISABLE;
    
    /* 读时序 - 使用保守参数 */
    fsmc_read_handle.AddressSetupTime = 0x0F;
    fsmc_read_handle.AddressHoldTime = 0x00;
    fsmc_read_handle.DataSetupTime = 60;
    fsmc_read_handle.BusTurnAroundDuration = 0;
    fsmc_read_handle.CLKDivision = 0;
    fsmc_read_handle.DataLatency = 0;
    fsmc_read_handle.AccessMode = FSMC_ACCESS_MODE_A;
    
    /* 写时序 - 使用保守参数 */
    fsmc_write_handle.AddressSetupTime = 15;
    fsmc_write_handle.AddressHoldTime = 0x00;
    fsmc_write_handle.DataSetupTime = 15;
    fsmc_write_handle.BusTurnAroundDuration = 0;
    fsmc_write_handle.CLKDivision = 0;
    fsmc_write_handle.DataLatency = 0;
    fsmc_write_handle.AccessMode = FSMC_ACCESS_MODE_A;
    
    HAL_SRAM_Init(&g_sram_handle, &fsmc_read_handle, &fsmc_write_handle);
    HAL_Delay(50);
    
    LCD_Debug("\r\n[LCD] Init start...\r\n");
    LCD_Debug("[LCD] LCD_BASE=0x%08lX\r\n", (uint32_t)LCD_BASE);
    
    /* 读取LCD ID */
    uint16_t tmp1, tmp2, tmp3, tmp4;
    LCD->LCD_REG = 0xD3;
    tmp1 = LCD->LCD_RAM;  /* dummy */
    tmp2 = LCD->LCD_RAM;  /* 0x00 */
    tmp3 = LCD->LCD_RAM;  /* 高字节 */
    tmp4 = LCD->LCD_RAM;  /* 低字节 */
    lcddev.id = (tmp3 << 8) | tmp4;
    LCD_Debug("[LCD] 0xD3 read: %04X %04X %04X %04X -> ID=%04X\r\n", tmp1, tmp2, tmp3, tmp4, lcddev.id);
    
    if(lcddev.id != 0x9341) {
        /* 尝试读取ST7789 */
        LCD->LCD_REG = 0x04;
        tmp1 = LCD->LCD_RAM;  /* dummy */
        tmp2 = LCD->LCD_RAM;
        tmp3 = LCD->LCD_RAM;
        tmp4 = LCD->LCD_RAM;
        lcddev.id = (tmp3 << 8) | tmp4;
        LCD_Debug("[LCD] 0x04 read: %04X %04X %04X %04X -> ID=%04X\r\n", tmp1, tmp2, tmp3, tmp4, lcddev.id);
        if(lcddev.id == 0x8552) lcddev.id = 0x7789;
        
        if(lcddev.id != 0x7789) {
            /* 尝试读取NT35310 */
            LCD->LCD_REG = 0xD4;
            tmp1 = LCD->LCD_RAM;  /* dummy */
            tmp2 = LCD->LCD_RAM;
            tmp3 = LCD->LCD_RAM;
            tmp4 = LCD->LCD_RAM;
            lcddev.id = (tmp3 << 8) | tmp4;
            LCD_Debug("[LCD] 0xD4 read: %04X %04X %04X %04X -> ID=%04X\r\n", tmp1, tmp2, tmp3, tmp4, lcddev.id);

            if(lcddev.id != 0x5310) {
                /* 尝试读取ILI9806: 先使能EXTC，再读0xD3 */
                LCD->LCD_REG = 0xFF;
                LCD->LCD_RAM = 0xFF; LCD->LCD_RAM = 0x98; LCD->LCD_RAM = 0x06;
                LCD->LCD_REG = 0xD3;
                tmp1 = LCD->LCD_RAM;  /* dummy */
                tmp2 = LCD->LCD_RAM;
                tmp3 = LCD->LCD_RAM;
                tmp4 = LCD->LCD_RAM;
                lcddev.id = (tmp3 << 8) | tmp4;
                LCD_Debug("[LCD] ILI9806 0xD3 read: %04X %04X %04X %04X -> ID=%04X\r\n", tmp1, tmp2, tmp3, tmp4, lcddev.id);
                /* ILI9806可能返回0x9806或其他值，如果前面都没识别到就当作ILI9806 */
                if(lcddev.id != 0x9806) {
                    LCD_Debug("[LCD] Force to ILI9806\r\n");
                    lcddev.id = 0x9806;
                }
            }
        }
    }
    
    LCD_Debug("[LCD] Final ID=%04X\r\n", lcddev.id);
    
    /* 根据ID执行初始化 */
    if(lcddev.id == 0x7789) {
        LCD_Debug("[LCD] Using ST7789 init\r\n");
        lcd_ex_st7789_reginit();
    } else if(lcddev.id == 0x9341) {
        LCD_Debug("[LCD] Using ILI9341 init\r\n");
        lcd_ex_ili9341_reginit();
    } else if(lcddev.id == 0x5310) {
        LCD_Debug("[LCD] Using NT35310 init\r\n");
        lcd_ex_nt35310_reginit();
    } else if(lcddev.id == 0x9806) {
        LCD_Debug("[LCD] Using ILI9806 init\r\n");
        lcd_ex_ili9806_reginit();
    } else {
        LCD_Debug("[LCD] Unknown ID, using ILI9341 init\r\n");
        lcd_ex_ili9341_reginit();
    }
    
    /* 设置默认参数 */
    lcddev.wramcmd = 0x2C;
    lcddev.setxcmd = 0x2A;
    lcddev.setycmd = 0x2B;
    lcddev.width = 240;
    lcddev.height = 320;
    lcddev.dir = 0;
    
    if(lcddev.id == 0x5310) {
        lcddev.width = 320;
        lcddev.height = 480;
    } else if(lcddev.id == 0x9806) {
        lcddev.width = 480;
        lcddev.height = 800;
    }
    
    LCD_Debug("[LCD] Size: %dx%d\r\n", lcddev.width, lcddev.height);
    
    /* 设置扫描方向 */
    lcd_scan_dir();
    
    /* 开启背光 */
    LCD_BL(1);
    LCD_Debug("[LCD] Backlight ON, clearing screen...\r\n");
    
    /* 清屏 */
    BSP_LCD_Clear(WHITE);
    LCD_Debug("[LCD] Init done!\r\n");
}

/**
  * @brief  清屏
  */
void BSP_LCD_Clear(uint16_t color)
{
    uint32_t i;
    uint32_t total = (uint32_t)lcddev.width * lcddev.height;
    
    lcd_set_window(0, 0, lcddev.width, lcddev.height);
    LCD->LCD_REG = lcddev.wramcmd;
    
    for(i = 0; i < total; i++) {
        LCD->LCD_RAM = color;
    }
}

/**
  * @brief  设置光标位置
  */
void BSP_LCD_SetCursor(uint16_t x, uint16_t y)
{
    LCD->LCD_REG = lcddev.setxcmd;
    LCD->LCD_RAM = x >> 8;
    LCD->LCD_RAM = x & 0xFF;
    LCD->LCD_REG = lcddev.setycmd;
    LCD->LCD_RAM = y >> 8;
    LCD->LCD_RAM = y & 0xFF;
}

/**
  * @brief  画点
  */
void BSP_LCD_DrawPoint(uint16_t x, uint16_t y, uint16_t color)
{
    BSP_LCD_SetCursor(x, y);
    LCD->LCD_REG = lcddev.wramcmd;
    LCD->LCD_RAM = color;
}

/**
  * @brief  填充矩形区域
  */
void BSP_LCD_Fill(uint16_t x1, uint16_t y1, uint16_t x2, uint16_t y2, uint16_t color)
{
    uint32_t i;
    uint16_t width = x2 - x1 + 1;
    uint16_t height = y2 - y1 + 1;
    uint32_t total = (uint32_t)width * height;
    
    lcd_set_window(x1, y1, width, height);
    LCD->LCD_REG = lcddev.wramcmd;
    
    for(i = 0; i < total; i++) {
        LCD->LCD_RAM = color;
    }
}

/**
  * @brief  显示单个字符 (8x16)
  */
void BSP_LCD_ShowChar(uint16_t x, uint16_t y, char c, uint16_t fc, uint16_t bc, uint8_t size)
{
    (void)size;
    uint8_t i, j;
    const uint8_t *font;
    
    if(c < ' ' || c > 'z') c = ' ';
    font = ASCII_8x16[c - ' '];
    
    lcd_set_window(x, y, 8, 16);
    LCD->LCD_REG = lcddev.wramcmd;
    
    for(i = 0; i < 16; i++) {
        for(j = 0; j < 8; j++) {
            if(font[i] & (0x80 >> j)) {
                LCD->LCD_RAM = fc;
            } else {
                LCD->LCD_RAM = bc;
            }
        }
    }
}

