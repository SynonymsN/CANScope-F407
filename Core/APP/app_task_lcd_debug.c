/* LCD硬件地址 - 正点原子探索者F407：Bank1_NE4, A6作为RS */
/* NE4基地址 = 0x6C000000, A6偏移 = 1<<(6+1) = 0x80 */
#define LCD_BASE_ADDR       ((uint32_t)0x6C000000)
#define LCD_REG_ADDR        (*((volatile uint16_t *)LCD_BASE_ADDR))
#define LCD_RAM_ADDR        (*((volatile uint16_t *)(LCD_BASE_ADDR + 0x80)))

/* FSMC SRAM句柄 */
static SRAM_HandleTypeDef hsram;

/**
  * @brief  初始化FSMC (用于LCD)
  */
static void LCD_FSMC_Init(void)
{
    FSMC_NORSRAM_TimingTypeDef read_timing;
    FSMC_NORSRAM_TimingTypeDef write_timing;
    GPIO_InitTypeDef gpio_init;
    
    /* 使能时钟 */
    __HAL_RCC_FSMC_CLK_ENABLE();
    __HAL_RCC_GPIOB_CLK_ENABLE();
    __HAL_RCC_GPIOD_CLK_ENABLE();
    __HAL_RCC_GPIOE_CLK_ENABLE();
    __HAL_RCC_GPIOF_CLK_ENABLE();
    __HAL_RCC_GPIOG_CLK_ENABLE();
    
    /* PB15 - 背光 */
    gpio_init.Pin = GPIO_PIN_15;
    gpio_init.Mode = GPIO_MODE_OUTPUT_PP;
    gpio_init.Pull = GPIO_PULLUP;
    gpio_init.Speed = GPIO_SPEED_FREQ_HIGH;
    HAL_GPIO_Init(GPIOB, &gpio_init);
    HAL_GPIO_WritePin(GPIOB, GPIO_PIN_15, GPIO_PIN_SET);  /* 背光ON */
    
    /* FSMC GPIO: PD0,1,4,5,8,9,10,14,15 */
    gpio_init.Pin = GPIO_PIN_0 | GPIO_PIN_1 | GPIO_PIN_4 | GPIO_PIN_5 |
                    GPIO_PIN_8 | GPIO_PIN_9 | GPIO_PIN_10 | GPIO_PIN_14 | GPIO_PIN_15;
    gpio_init.Mode = GPIO_MODE_AF_PP;
    gpio_init.Pull = GPIO_PULLUP;
    gpio_init.Speed = GPIO_SPEED_FREQ_HIGH;
    gpio_init.Alternate = GPIO_AF12_FSMC;
    HAL_GPIO_Init(GPIOD, &gpio_init);
    
    /* PE7-15 */
    gpio_init.Pin = GPIO_PIN_7 | GPIO_PIN_8 | GPIO_PIN_9 | GPIO_PIN_10 |
                    GPIO_PIN_11 | GPIO_PIN_12 | GPIO_PIN_13 | GPIO_PIN_14 | GPIO_PIN_15;
    HAL_GPIO_Init(GPIOE, &gpio_init);
    
    /* PF12: A6 (RS) */
    gpio_init.Pin = GPIO_PIN_12;
    HAL_GPIO_Init(GPIOF, &gpio_init);
    
    /* PG12: NE4 (CS) */
    gpio_init.Pin = GPIO_PIN_12;
    HAL_GPIO_Init(GPIOG, &gpio_init);
    
    /* FSMC配置 */
    hsram.Instance = FSMC_NORSRAM_DEVICE;
    hsram.Extended = FSMC_NORSRAM_EXTENDED_DEVICE;
    hsram.Init.NSBank = FSMC_NORSRAM_BANK4;
    hsram.Init.DataAddressMux = FSMC_DATA_ADDRESS_MUX_DISABLE;
    hsram.Init.MemoryType = FSMC_MEMORY_TYPE_SRAM;
    hsram.Init.MemoryDataWidth = FSMC_NORSRAM_MEM_BUS_WIDTH_16;
    hsram.Init.BurstAccessMode = FSMC_BURST_ACCESS_MODE_DISABLE;
    hsram.Init.WaitSignalPolarity = FSMC_WAIT_SIGNAL_POLARITY_LOW;
    hsram.Init.WaitSignalActive = FSMC_WAIT_TIMING_BEFORE_WS;
    hsram.Init.WriteOperation = FSMC_WRITE_OPERATION_ENABLE;
    hsram.Init.WaitSignal = FSMC_WAIT_SIGNAL_DISABLE;
    hsram.Init.ExtendedMode = FSMC_EXTENDED_MODE_ENABLE;
    hsram.Init.AsynchronousWait = FSMC_ASYNCHRONOUS_WAIT_DISABLE;
    hsram.Init.WriteBurst = FSMC_WRITE_BURST_DISABLE;
    
    /* 读时序 (保守) */
    read_timing.AddressSetupTime = 15;
    read_timing.AddressHoldTime = 0;
    read_timing.DataSetupTime = 60;
    read_timing.BusTurnAroundDuration = 0;
    read_timing.CLKDivision = 0;
    read_timing.DataLatency = 0;
    read_timing.AccessMode = FSMC_ACCESS_MODE_A;
    
    /* 写时序 */
    write_timing.AddressSetupTime = 9;
    write_timing.AddressHoldTime = 0;
    write_timing.DataSetupTime = 9;
    write_timing.BusTurnAroundDuration = 0;
    write_timing.CLKDivision = 0;
    write_timing.DataLatency = 0;
    write_timing.AccessMode = FSMC_ACCESS_MODE_A;
    
    HAL_SRAM_Init(&hsram, &read_timing, &write_timing);
}

/**
  * @brief  写命令
  */
static void LCD_WriteCmd(uint16_t cmd)
{
    LCD_REG_ADDR = cmd;
}

/**
  * @brief  写数据
  */
static void LCD_WriteData(uint16_t data)
{
    LCD_RAM_ADDR = data;
}

