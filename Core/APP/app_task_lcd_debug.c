/**
  * @brief  画点
  */
static void LCD_DrawPoint(uint16_t x, uint16_t y, uint16_t color)
{
    LCD_WriteCmd(0x2A);
    LCD_WriteData(x >> 8); LCD_WriteData(x & 0xFF);
    LCD_WriteData(x >> 8); LCD_WriteData(x & 0xFF);
    
    LCD_WriteCmd(0x2B);
    LCD_WriteData(y >> 8); LCD_WriteData(y & 0xFF);
    LCD_WriteData(y >> 8); LCD_WriteData(y & 0xFF);
    
    LCD_WriteCmd(0x2C);
    LCD_RAM_ADDR = color;
}

/**
  * @brief  显示一个字符 (8x16) - 纵向取模格式
  *         字库格式: 先左半8行(上8像素)，再左半8行(下8像素)
  */
static void LCD_ShowChar(uint16_t x, uint16_t y, char ch, uint16_t fc, uint16_t bc)
{
    uint8_t i, j;
    uint8_t idx;
    uint8_t data;
    
    if(ch < 32 || ch > 90) ch = ' ';
    idx = ch - 32;
    
    /* 字库是纵向取模: Font8x16[idx][0-7]是左边8列的上半部分
       Font8x16[idx][8-15]是左边8列的下半部分 */
    for(i = 0; i < 8; i++) {  /* 8列 */
        /* 上半部分 (y到y+7) */
        data = Font8x16[idx][i];
        for(j = 0; j < 8; j++) {
            if(data & (1 << j)) {
                LCD_DrawPoint(x + i, y + j, fc);
            } else if(bc != fc) {
                LCD_DrawPoint(x + i, y + j, bc);
            }
        }
        /* 下半部分 (y+8到y+15) */
        data = Font8x16[idx][i + 8];
        for(j = 0; j < 8; j++) {
            if(data & (1 << j)) {
                LCD_DrawPoint(x + i, y + 8 + j, fc);
            } else if(bc != fc) {
                LCD_DrawPoint(x + i, y + 8 + j, bc);
            }
        }
    }
}

/**
  * @brief  显示字符串
  */
static void LCD_ShowString(uint16_t x, uint16_t y, const char *str, uint16_t fc, uint16_t bc)
{
    while(*str) {
        LCD_ShowChar(x, y, *str, fc, bc);
        x += 8;
        str++;
    }
}

