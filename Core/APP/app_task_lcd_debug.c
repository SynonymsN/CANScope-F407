/**
  * @brief  LCD清屏
  */
static void LCD_Clear(uint16_t color)
{
    uint32_t i;
    
    LCD_WriteCmd(0x2A);
    LCD_WriteData(0); LCD_WriteData(0);
    LCD_WriteData(1); LCD_WriteData(0xDF);  /* 480-1 */
    
    LCD_WriteCmd(0x2B);
    LCD_WriteData(0); LCD_WriteData(0);
    LCD_WriteData(3); LCD_WriteData(0x1F);  /* 800-1 */
    
    LCD_WriteCmd(0x2C);  /* Write RAM */
    for(i = 0; i < 480UL * 800UL; i++) {
        LCD_RAM_ADDR = color;
    }
}

